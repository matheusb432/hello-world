import math # Importa as funcoes do objeto math, no caso, isso serve como uma biblioteca de funcoes para facilitar o uso de matematica avancada.

print(math.ceil(2.1)) # Arredonda o valor decimal para cima.

x = 2.9
print(round(x)) # Arredonda x para o inteiro mais proximo
print(abs(-2.9)) # Retorna o modulo de -2.9, ou seja, |-2.9| = 2.9, resultado sempre positivo.
