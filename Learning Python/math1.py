print(10 + 3)    # Soma
print(10 - 3)    # Subtracao
print(10 * 3)    # Multiplicacao
print(10 / 3)    # Divisao (float)
print(10 // 3)   # Divisao (integer)
print(10 % 3)    # Resto da divisao entre 10 e 3
print(10 ** 3)   # Potenciacao, 10 elevado a 3
x = 10
x = x + 3    # incrementacao de x
x += 3       # mesmo que x = x+3, o mesmo vai para outras operacoes
print(x)